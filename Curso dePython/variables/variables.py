#Declarar variable:
nombre = "Leonardo"
print(nombre)

#otra forma de declarar variables es escribiendo varias palabras pero con mayúsculas
#nobre_completo_de_alumno
numero = 11
numero += 4
print(numero)

#Concatenación con +:

bienvenida = "Hola " + nombre + " ¿cómo estás? "
print(bienvenida)

#Concatenación con f-strings:
bienvenida = f"Hola {nombre} ¿cómo estás?"
print(bienvenida)

#para eliminar una variable:
del nombre

#operadores de pertenencia:
print("hola" in bienvenida)
print("hola" not in bienvenida)


