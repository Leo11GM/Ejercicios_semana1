#Comparador de palabras
palabra1 = input("Ingrese la palabra 1: ")
palabra2 = input("Ingrese la palabra 2: ")
if palabra1 == palabra2 :
    print("Las palabras 1 y 2 son iguales.")
else:
    print("Las  palabras 1 y 2 son diferentes.")
if len(palabra1) == len(palabra2) :
    print("La longitud de la palabra 1 y 2 es igual.")
else:
    print("La longitud de la palabra 1 y 2 es diferente.")    
